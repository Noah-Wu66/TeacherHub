export function buildSystemPrompt(userRole: string, voiceMode?: boolean) {
  const role = String(userRole || 'user');
  const isTeacherOrAdmin = ['teacher', 'admin', 'superadmin'].includes(role);
  const base = isTeacherOrAdmin ? TEACHER_SYSTEM_PROMPT : STUDENT_SYSTEM_PROMPT;
  return base + (voiceMode ? VOICE_MODE_PROMPT : '');
}


